# counter.py

def inc(x):
  """
  Increments the value of x
  
  >>> inc(4)
  5
  
  """
  return x+1
  
def dec(x):
  """
  Decrements the value of x
  
  >>> dec(5)
  4
  
  """
  return x-1

def inc_by_2(x):
  """
  Increments the value of x by 2
  
  >>> inc_by_2(4)
  6
  
  """
  return x + 2

def dec_by_2(x):
  """
  Decrements the value of x by 2
  
  >>> dec_by_2(6)
  4
  
  """
  return x - 2
